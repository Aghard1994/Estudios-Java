package estudiojava;
public class Matematicas {
    public static void main(String[] args) {
        //Math.max() encuentra el método más alto (x, y).
        System.out.println(Math.max(5,10));
        
        //Math.min() encuentra el método más bajo (x, y).
        System.out.println(Math.min(5,10));
        
        //Math.sqrt() encuentra la raíz cuadrada de x.
        System.out.println(Math.sqrt(3));
        
        //Math.abs() devuelve el valor absoluto (positivo) de x
        System.out.println(Math.abs(-5.3));
        
        //Math.random() devuelve un número aleatorio entre 0 (inclusivo) y 1 (exclusivo)
        System.out.println(Math.random());
    }
}
