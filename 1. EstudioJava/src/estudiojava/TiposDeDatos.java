
package estudiojava;

public class TiposDeDatos {
    public static void main(String[] args) {
         //Tipos de datos primitivos
//Un tipo de datos primitivo especifica el tamaño y el tipo de valores variables, y no tiene métodos adicionales.

//Hay ocho tipos de datos primitivos en Java:
    
    /* 
    byte -> puede almacentar números enteros del -128 al 127
    short -> puede almacentar números enteros del -32768 al 32767
    int -> puede almacentar números enteros del -2147483648 al 2147483647
    long ->
    float
    double
    boolean
    char
    */
    byte numero = 100;
    System.out.println(numero);
    
    short numero2 = 5000;
    System.out.println(numero2);
    
    int numero3 = 100000;
    System.out.println(numero3);
    
    }
   
    
    
}
